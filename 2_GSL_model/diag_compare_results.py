"""
compare_results.py
Compare Results/ (refactored plot_Figure3_4a_Xin.m, run with TONIC ForcingData)
against the original TONIC Results/ (generated by original plot_Figure3_4a_Xin.m).

Both use ftag='Corr' so output files end in .Corr.res.

Scenario label mapping (same scenario, different naming convention):
  Original TONIC:  nat, SSP126, SSP245, SSP370, SSP585, 10000pgc
  Refactored:      nat, SSP1-26, SSP2-45, SSP3-70, SSP5-85, 10000pgc
"""

import numpy as np
import os

DIR_ORIG = "/Users/bo20541/Library/CloudStorage/OneDrive-UniversityofBristol/TONIC-Oligocene/Emulator_Xin/2025_Bristol_5D_v002_Xin/Conceptual-GSL/PosivaSKB-master/Results"
DIR_NEW  = "Results"
FTAG     = "Corr"

# (label_in_original, label_in_new)
SHARED = [
    ("nat",      "nat"),
    ("SSP126",   "SSP1-26"),
    ("SSP245",   "SSP2-45"),
    ("SSP370",   "SSP3-70"),
    ("SSP585",   "SSP5-85"),
    ("10000pgc", "10000pgc"),
]

TYPES = ["ice_volume", "Rnum", "temp_v"]

print(f"Comparing TONIC original vs refactored (both using TONIC ForcingData CO2)\n")
print(f"  ORIG: {DIR_ORIG}")
print(f"  NEW:  {DIR_NEW}\n")
print(f"{'Type':<18} {'Scenario mapping':<24} {'MaxAbsDiff':>12} {'Match?':>8}")
print("-" * 66)

any_fail = False
for typ in TYPES:
    for orig_scen, new_scen in SHARED:
        fname_orig = f"{typ}_AG_-0.5_1myr_AP_LHCsamps_{orig_scen}.{FTAG}.res"
        fname_new  = f"{typ}_AG_-0.5_1myr_AP_LHCsamps_{new_scen}.{FTAG}.res"
        path_orig  = os.path.join(DIR_ORIG, fname_orig)
        path_new   = os.path.join(DIR_NEW,  fname_new)

        if not os.path.exists(path_orig):
            print(f"  MISSING in original: {fname_orig}")
            any_fail = True
            continue
        if not os.path.exists(path_new):
            print(f"  MISSING in new:      {fname_new}")
            any_fail = True
            continue

        orig = np.loadtxt(path_orig, comments="%")
        new  = np.loadtxt(path_new,  comments="%")

        if orig.shape != new.shape:
            print(f"  SHAPE MISMATCH: {fname_orig} {orig.shape} vs {fname_new} {new.shape}")
            any_fail = True
            continue

        diff = np.max(np.abs(orig - new))
        match = "OK" if diff < 1e-6 else "DIFF"
        if match != "OK":
            any_fail = True
        label = f"{orig_scen} vs {new_scen}"
        print(f"  {typ:<18} {label:<24} {diff:>12.2e} {match:>8}")

print()
if any_fail:
    print("Some differences found — investigate above.")
else:
    print("All shared scenarios match: refactoring is validated against TONIC original.")
